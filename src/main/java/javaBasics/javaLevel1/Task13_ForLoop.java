package javaBasics.javaLevel1;

public class Task13_ForLoop {
    public static void main(String[] args) {
        for(int a=0; a<10; a++){
            System.out.println("h:"+a);
        }

        for(int i=0; i<=10; i++){
            System.out.println("i: " +i);
        }

        for(int j=20; j>=5; j--){
            System.out.println("j: " +j);
        }
    }
}
