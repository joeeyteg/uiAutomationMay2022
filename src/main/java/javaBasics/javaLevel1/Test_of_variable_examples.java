package javaBasics.javaLevel1;

public class Test_of_variable_examples {
    public static void main(String[] args) {

        //string variables will store texts like String name = "Man"
        String name = "John Wallace, the Doctor";
        System.out.println(name);
        String occupation = "Time Traveller";
        System.out.println(occupation);
        String city = "Glasgow,";
        String country = "Scotland";
        String placeOfBirth = city + country;
        System.out.println(placeOfBirth);


        //int variables will store numbers
        int A = 35;
        System.out.println("age:" + A);
        //char variable will store single characters like char = 'X'
        char BT = 'O';
        System.out.println("bloodType:"+BT);

    }
}
