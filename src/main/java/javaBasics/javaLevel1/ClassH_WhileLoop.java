package javaBasics.javaLevel1;

public class ClassH_WhileLoop {
    public static void main(String[] args) {

//while loop:
        System.out.println("while loop:");
        int a = 1;
        while (a < 15) {
            System.out.println(a);
            a++;

        }

        //do while
        System.out.println("do while:");
        int k = 5;
        do{
            System.out.println(k);
            k++;
        }while (k<15);


    }



}
